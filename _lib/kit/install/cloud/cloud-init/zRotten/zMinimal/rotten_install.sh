		echo 'root:'$(_rand_passwd 15) | sudo -n chpasswd
		echo 'root:'$(_rand_passwd 32) | sudo -n chpasswd
		
		echo 'user:'$(_rand_passwd 15) | sudo -n chpasswd
		echo 'user:'$(_rand_passwd 32) | sudo -n chpasswd
		
		
